package edu.htu.upskilling.java.lecture6.circle;

public class Circle_V3 {
	int rad;
	
	public double calculateRound() {
		return 2 * rad * 3.14;
		//System.out.println("Round =" + round);
	}

	public double calculateArea() {
		return rad * rad * 3.14;
//		System.out.println("Area =  " + area);
	}
}
