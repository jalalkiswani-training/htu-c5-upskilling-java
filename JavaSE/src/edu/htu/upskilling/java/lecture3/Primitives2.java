package edu.htu.upskilling.java.lecture3;

public class Primitives2 {
	public static void main(String[] args) {
		byte b=10; //1-byte (7-bit + 1 sign-bit)
		short s=100; //2-bytes , sign-bit (total 15 bit)
		char c='a'; //2-bytes , no sign-bit (total 16 bit)
		int i=1000;//4-bytes
		float f=10.2f;//4-bytes
		long l=100000;//8-bytes
		double d=100_000_222.345; //8-bytes
		boolean b1=true;
		boolean b2=false;		
	}
}
